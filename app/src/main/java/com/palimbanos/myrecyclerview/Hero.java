package com.palimbanos.myrecyclerview;

public class Hero {
    private String name;
    private String alamat;
    private String harga;
    private String detail;
    private String telpon;
    private String maps;
    private int photo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    String getAlamat() {
        return alamat;
    }

    void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    String getHarga() {
        return harga;
    }

    void setHarga(String harga) {
        this.harga = harga;
    }

    String getDetail() {
        return detail;
    }

    void setDetail(String detail) {
        this.detail = detail;
    }

    String getTelpon() {
        return telpon;
    }

    void setTelpon(String telpon) {
        this.telpon = telpon;
    }

    String getMaps() {
        return maps;
    }

    void setMaps(String maps) {
        this.maps = maps;
    }

    int getPhoto() {
        return photo;
    }

    void setPhoto(int photo) {
        this.photo = photo;
    }
}
